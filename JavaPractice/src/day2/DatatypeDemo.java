package day2;

public class DatatypeDemo {

	public static void main(String[] args) {
		
		int c=300, d=200;
        System.out.println(c);
        System.out.println(d);
        System.out.println(c-d);
        
        byte by=120;
        System.out.println(by);
        
        short sh=250;
        System.out.println(sh);
        
        long l=123456789;
        System.out.println(l);
        
        
	}

}
