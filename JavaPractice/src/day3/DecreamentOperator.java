package day3;

public class DecreamentOperator {

	public static void main(String[] args) {
		
       int a=100;
       a=a-1;
       System.out.println(a);
       
       int b=200;
       b--;
       System.out.println(b);
       
       // post decreament
       int d=20;
       int res=d--;
       System.out.println(res);
       
       // pre decreament
       
       int f=30;
       int mark=--f;
       System.out.println(mark);
	
	
	}

}
